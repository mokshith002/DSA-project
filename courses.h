map<string, course>
findCourses(string year)
{
    map<string, course> result;

    if (year == "2021")
        result = first_year;

    else if (year == "2020")
        result = second_year;
    
    else if (year == "2019")
        result = third_year;
    
    else if (year == "2018")
        result = fourth_year;
    

    for(auto [classCode, c] : result){
        courseSections[c.courseCode].insert(c.section.substr(0, 1));
    }

    return result;
}
